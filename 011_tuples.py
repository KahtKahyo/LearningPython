# tuples!

# tuples are basically list but are immutable!

tuple_example = (1, 3.14, "John")
print("Length: ", len(tuple_example))
print("1st: ", tuple_example[0])
print("Last: ", tuple_example[-1])
print("1st 2: ", tuple_example[0:2])
print("Every other: ", tuple_example[0:-1:2])
print("Reverse: ", tuple_example[::-1])
